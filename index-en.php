<script>
    $(function(){
        $('.js-textarea').keyup(function(){
            $('.js-count-textarea').html($('.js-textarea').val().length);
        });

        let today = new Date(),
            d = String(today.getDate()).padStart(2, '0'),
            m = String(today.getMonth() + 1).padStart(2, '0'),
            y = today.getFullYear();

        today = d + '/' + m + '/' + y;
        $('.js-input-date').val(today);
        $('.js-input-fullname-1').keyup(function(){
           $('.js-input-fullname-2').val($('.js-input-fullname-1').val());
       });
        $('.js-input-fullname-2').keyup(function(){
            $('.js-input-fullname-1').val($('.js-input-fullname-2').val());
        });






         //SEND DATA TO DATABASE
        $('#sms').click(function(){

            var name = $('#fullname').val();
            var trade = $('#checkbox-3').prop( "checked" )? 1 : 0;
            var portfolio = $('#checkbox-4').prop( "checked" )? 1 : 0;
            var experience = $('#checkbox-5').prop( "checked" )? 1 : 0;

            

            if ($("form")[0].checkValidity() ) {

                var phone = $('#phone').val().replace(/\s+/g, '');

                $.ajax({
                    url: 'codeGeneratorEn.php',
                    type: 'POST',
                    data: { 
                        user: name, 
                        phone: phone, 
                        trade:trade, 
                        portfolio:portfolio, 
                        experience:experience,
                        captcha: grecaptcha.getResponse(),
                    },
                    success: function(response){

                        if(response === 'true'){
                            swalFireSmsSent();
                            $('.sms-off').addClass('sms-on');
                        }else{
                            swalFireError();
                            $('#sms').text('Try again');
                        }

                    },
                    error: function(XMLHttpRequest, textStatus, errorThrown) { 
                               
                        swalFireError();

                    }
                });
            }
            
        });

         //VERIFY SMS
        $('#verify').click(function(){

            var phone = $('#phone').val().replace(/\s+/g, '');
            var code = $('#smscode').val();
            var user = $('#fullname').val();

            $.ajax({
                url: 'codeCheck.php',
                type: 'POST',
                data: { phone: phone, code: code, user: user },
                success: function(output){
                    if(output=='1'){

                        var phone = $('#phone').val().replace(/\s+/g, ''); 
                        var user = $('#fullname').val();

                        var trade = $('#checkbox-3').prop( "checked" )? "yes" : "no";
                        var portfolio = $('#checkbox-4').prop( "checked" )? "yes" : "no";
                        var experience = $('#checkbox-5').prop( "checked" )? "yes" : "no";
                        var date = $('#sign-date').val();

                        //SEND email
                        sendMail(phone, user, trade, portfolio, experience, date)

                        //POPUP SUCCESS
                        swalFireSmsSuccess();

                    }else{

                        //POPUP FAIL
                        swalFireSmsNotMatch();
                    }

                }
            });

            
        });

        //SendMail.php send data
        function sendMail(phone, user, trade, portfolio, experience, date){
            var lang = 'en';
             $.ajax({
                    url: 'sendMail.php',
                    type: 'POST',
                    data: { phone: phone, user: user, trade:trade, portfolio:portfolio, experience:experience, date:date, lang:lang},
                    success: function(response){
                     console.log(response);
                    }
                });
            
            
        }

        

        //Popup Sms Error
        function swalFireError(){
            Swal.fire({
                icon: 'error',
                title: 'Oops...',
                text: 'Something went wrong! Please try again.',
            });
        }

        //Popup Sms Success Sent
        function swalFireSmsSent(){
            Swal.fire({
                position: 'center',
                type: 'success',
                title: 'We have successfully sent your verfication code.',
                //confirmButtonText: '<a href="https://live-cosmos.globtrex.com/trading-platform/#login">OK</a>',
                timer: 1000,
                onBeforeOpen: () => {
                    Swal.showLoading()
                    timerInterval = setInterval(() => {
                        Swal.getContent().querySelector('b')
                        .textContent = Swal.getTimerLeft()
                    }, 100)
                },
                onClose: () => {
                    clearInterval(timerInterval)
                }
            });
        }

        // Popup Sms Success match
        function swalFireSmsSuccess(){
             Swal.fire({
                position: 'center',
                type: 'success',
                title: 'Your application has been submitted. We will contact you shortly, once your application has been approved.',
                confirmButtonText: '<a href="https://live-cosmos.globtrex.com/trading-platform/#login">OK</a>'
            }).then(function (result) {
              if (result.value) {
                window.location = "https://live-cosmos.globtrex.com/trading-platform/#login";
                }
            });
        }

        // Popup Sms Not match
        function swalFireSmsNotMatch(){
            Swal.fire({
                position: 'center',
                type: 'error',
                title: 'The verification code you provided does not match. ',
                showConfirmButton: false,
                timer: 1500
            });
        }


});
